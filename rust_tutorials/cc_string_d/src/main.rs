// Storing greetings in different languages in strings

fn main() {
    let hello_a = String::from("السلام عليكم");
    let hello_b = String::from("Dobrý den");
    let hello_c = String::from("Hello");
    let hello_d = String::from("שָׁלוֹם");
    let hello_e = String::from("नमस्ते");
    let hello_f = String::from("こんにちは");
    let hello_g = String::from("안녕하세요");
    let hello_h = String::from("你好");
    let hello_i = String::from("Olá");
    let hello_j = String::from("Здравствуйте");
    let hello_k = String::from("Hola");
     
    println!("{hello_a}   {hello_b}   {hello_c}   {hello_d}   {hello_e}   {hello_f}   {hello_g}   {hello_h}   {hello_i}   {hello_j}   {hello_k}");
}
